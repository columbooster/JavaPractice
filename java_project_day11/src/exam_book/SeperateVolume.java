package exam_book;

public class SeperateVolume implements Lendable {
	
	private String requestNo;
	private String bookTitle;
	private String writer;
	private String borrower;
	private String checkOutDate;
	private int state;
	
	public SeperateVolume(String requestNo, String bookTitle, String writer) {
		this.requestNo = requestNo;
		this.bookTitle = bookTitle;
		this.writer = writer;
	}
	

	@Override
	public void checkOut(String borrower, String date) { //대출 메서드
		if(state !=0) // 대여중이라면 
			return;		// return 값;  return; - 메서드를 종료하겠다는 의미.
		this.borrower = borrower;
		this.checkOutDate = date;
		this.state = 1; // 대출상태
		System.out.println("*"+ bookTitle + "("+ writer + ") 이(가) 대출되었습니다.");
		System.out.println("대출인 : " + this.borrower);
		System.out.println("대출일 : " + this.checkOutDate + "\n");
	}

	@Override
	public void checkIn() { //반납 메서드
		if(state == 0) { // 이미 반납 처리가 되었다면
			System.out.println("이미 반납처리가 완료되었습니다.");
			return;
		}
		this.borrower = null;
		this.checkOutDate = null;
		this.state = 0; //반납상태
		System.out.println("*"+ bookTitle + "이 반납되었습니다.");
	}


	public String getRequestNo() {
		return requestNo;
	}


	public void setRequestNo(String requestNo) {
		this.requestNo = requestNo;
	}


	public String getBookTitle() {
		return bookTitle;
	}


	public void setBookTitle(String bookTitle) {
		this.bookTitle = bookTitle;
	}


	public String getWriter() {
		return writer;
	}


	public void setWriter(String writer) {
		this.writer = writer;
	}


	public String getBorrower() {
		return borrower;
	}


	public void setBorrower(String borrower) {
		this.borrower = borrower;
	}


	public String getChekOutDate() {
		return checkOutDate;
	}


	public void setChekOutDate(String chekOutDate) {
		this.checkOutDate = chekOutDate;
	}


	public int getState() {
		return state;
	}


	public void setState(int state) {
		this.state = state;
	}
	
	
	
	

}
